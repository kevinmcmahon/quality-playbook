#!/usr/bin/env python3
"""Score the requirements validation experiment results.

For each (condition, defect) pair, reads the review output and determines
whether the specific defect was found. Uses the same defect definitions
and scoring guidance from nsq_defects_v2.json.

Produces a summary table and detailed per-defect results.
"""

import json
import os
import re
import sys

EXPERIMENT_DIR = os.path.dirname(os.path.abspath(__file__))
DEFECTS_FILE = os.path.join(EXPERIMENT_DIR, "..", "..", "benchmarks", "nsq_defects_v2.json")

CONDITIONS = ["control", "specific", "abstract", "v1212"]

# The 16 derivable defects
DERIVABLE = [
    "NSQ-04", "NSQ-12", "NSQ-14", "NSQ-19", "NSQ-22", "NSQ-33",
    "NSQ-36", "NSQ-37", "NSQ-39", "NSQ-41", "NSQ-42", "NSQ-44",
    "NSQ-47", "NSQ-48", "NSQ-50", "NSQ-55",
]

# Control baseline from v3 scoring (for comparison)
CONTROL_BASELINE = {
    "NSQ-04": True, "NSQ-12": True, "NSQ-14": False, "NSQ-19": True,
    "NSQ-22": True, "NSQ-33": True, "NSQ-36": False, "NSQ-37": True,
    "NSQ-39": False, "NSQ-41": True, "NSQ-42": True, "NSQ-44": False,
    "NSQ-47": True, "NSQ-48": True, "NSQ-50": True, "NSQ-55": False,
}


def load_defects():
    """Load defect definitions for scoring guidance."""
    with open(DEFECTS_FILE) as f:
        data = json.load(f)
    return {d["id"]: d for d in data["defects"]}


def read_review(condition, defect_id):
    """Read the review output for a condition/defect pair.

    Checks both the reviews directory (where the prompt saves findings)
    and the outputs directory (raw copilot output).
    """
    # First check the review file (generated by the model)
    review_path = os.path.join(EXPERIMENT_DIR, f"reviews_{condition}", f"{defect_id}_review.md")
    if os.path.exists(review_path):
        with open(review_path) as f:
            return f.read()

    # Fall back to raw output
    output_path = os.path.join(EXPERIMENT_DIR, f"outputs_{condition}", f"{defect_id}.output.txt")
    if os.path.exists(output_path):
        with open(output_path) as f:
            return f.read()

    return None


def check_defect_found(review_text, defect):
    """Check if a specific defect was identified in the review.

    Uses the scoring_guidance from nsq_defects_v2.json — the
    what_a_reviewer_would_say patterns for positive matches and
    not_sufficient patterns for false positive filtering.
    """
    if not review_text:
        return False, None

    text_lower = review_text.lower()
    guidance = defect.get("scoring_guidance", {})

    # Check positive patterns
    positive_phrases = guidance.get("what_a_reviewer_would_say", [])
    for phrase in positive_phrases:
        # Convert guidance phrases to flexible regex
        pattern = re.escape(phrase.lower())
        # Allow flexible whitespace
        pattern = pattern.replace(r"\ ", r"\s+")
        if re.search(pattern, text_lower):
            return True, phrase

    # Also check for key mechanism keywords from the violation
    violation = defect.get("violation", {})
    location = violation.get("location", "").lower()
    mechanism = violation.get("mechanism", "").lower()

    # Build location + mechanism pattern
    if location and mechanism:
        loc_words = [w for w in re.split(r'[/(),\s]+', location) if len(w) > 3]
        mech_words = [w for w in re.split(r'[/(),\s]+', mechanism) if len(w) > 3]

        for loc in loc_words:
            for mech in mech_words:
                if loc.lower() in text_lower and mech.lower() in text_lower:
                    return True, f"location:{loc} + mechanism:{mech}"

    return False, None


def score_all():
    defects = load_defects()
    results = {}

    for condition in CONDITIONS:
        results[condition] = {}
        for did in DERIVABLE:
            review = read_review(condition, did)
            if review is None:
                results[condition][did] = {"found": None, "evidence": "NO REVIEW FILE"}
                continue

            defect = defects.get(did, {})
            found, evidence = check_defect_found(review, defect)
            results[condition][did] = {"found": found, "evidence": evidence or ""}

    return results


def print_results(results):
    # Header
    print(f"{'Defect':8s} | {'Baseline':8s} | {'Control':8s} | {'Specific':8s} | {'Abstract':8s} | {'v1.2.12':8s}")
    print("-" * 70)

    for did in DERIVABLE:
        baseline = "YES" if CONTROL_BASELINE.get(did) else "no"
        row = [f"{did:8s}", f"{baseline:8s}"]
        for cond in CONDITIONS:
            entry = results[cond][did]
            if entry["found"] is None:
                row.append(f"{'---':8s}")
            elif entry["found"]:
                row.append(f"{'YES':8s}")
            else:
                row.append(f"{'no':8s}")
        print(" | ".join(row))

    # Totals
    print("-" * 70)
    row = [f"{'TOTAL':8s}", f"{sum(CONTROL_BASELINE.values()):3d}/16  "]
    for cond in CONDITIONS:
        found = sum(1 for did in DERIVABLE if results[cond][did]["found"])
        missing = sum(1 for did in DERIVABLE if results[cond][did]["found"] is None)
        if missing > 0:
            row.append(f"{found:2d}/16*  ")
        else:
            row.append(f"{found:2d}/16   ")
    print(" | ".join(row))

    # Key metrics
    print("\n=== Key Metrics ===")
    missed_by_baseline = [d for d, v in CONTROL_BASELINE.items() if not v]
    print(f"Defects missed by original control: {missed_by_baseline}")
    for cond in CONDITIONS:
        recovered = [d for d in missed_by_baseline if results[cond][d]["found"]]
        print(f"  {cond}: recovered {len(recovered)}/5 — {recovered}")

    print("\nDefects missed by ALL conditions in original benchmark: NSQ-36, NSQ-39, NSQ-44")
    for cond in CONDITIONS:
        found = [d for d in ["NSQ-36", "NSQ-39", "NSQ-44"] if results[cond][d]["found"]]
        print(f"  {cond}: found {len(found)}/3 — {found}")


def save_results(results):
    output_path = os.path.join(EXPERIMENT_DIR, "experiment_results.json")
    with open(output_path, "w") as f:
        json.dump(results, f, indent=2)
    print(f"\nResults saved to {output_path}")


if __name__ == "__main__":
    results = score_all()
    print_results(results)
    save_results(results)
